package junit4DemoEx;

public class MathCalc {

	public int add(int a,int b) {
		return a+b;
	}
		
	public double areaOfCircle(int radius) {
		return 3.14*radius*radius;
	}
	
	public int divide(int a, int b) {
		return a/b;
	}
	
	public boolean isOdd(int number)
	{
		return number%2!=0;
	}
}
